        <h3 class="a-h3-heading">Importance of on-time payments</h3>
        <p class="a-paragraph-regular">Payments made on time have a considerable impact on your <b>credit score</b> for several reasons:</p>
        <ul>
            <li><p><b>Demonstration of reliability:</b> Punctual payments show lenders that you are a reliable and responsible borrower.</p></li>
            <li><p><b>Positive history:</b> Each on-time payment contributes to building a positive credit history, which is particularly important if you have a relatively young credit file.</p></li>
            <li><p><b>Avoiding penalties:</b> Late payments can result in additional fees and higher interest rates, in addition to damaging your score.</p></li>
            <li><p><b>Long-term impact:</b> Late payments can remain on your credit file for up to 7 years, affecting your <b>credit score</b> over a long period.</p></li>
        </ul>
        
        <h2 class="a-h2-heading">Strategy 2: Reducing credit utilization rate</h2>
        <p class="a-paragraph-regular">The credit utilization rate, which represents the percentage of your available credit that you're using, is a crucial factor 
        in calculating your <b>credit score</b>. To achieve this, several strategies are available to you: make multiple payments throughout the month, request an increase 
        in your credit limits (without increasing your spending), and use your credit cards strategically by spreading out your expenses.</p>
