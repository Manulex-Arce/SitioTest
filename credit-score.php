        <h3 class="a-h3-heading">What is a <b>credit score</b>?</h3>
        <p class="a-paragraph-regular">A <b>credit score</b> is a numerical score that represents your financial reliability in the eyes of lenders. In Canada, 
        this score generally ranges from 300 to 900, with 900 being the best possible score. Your <b>credit score</b> is calculated by credit agencies, mainly 
        Equifax and TransUnion, based on the information contained in your credit file.</p>
        
        <h2 class="a-h2-heading">Strategy 1: Timely payment of bills</h2>
        <p class="a-paragraph-regular">Timely payment of your bills is the cornerstone of a good <b>credit score</b>. It's the most important factor in calculating your 
        score, accounting for about 35% of its weight. At Express Cash, we cannot stress enough the importance of this financial habit.</p>
